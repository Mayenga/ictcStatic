                    <h3>Professional Registration (IPRS)</h3>
                    <p class="fst-italic">
                    Moving towards industrialization within the information age requires exploitation of digital technologies and high – tech systems. These digital technologies are changing based on the pace of technological advancements hence need for the national to have experts in various ICT fields. The registration process shall facilitate promotion and elevation of the arts, science and general knowledge of Information and Communications Technology (ICT) in Tanzania, and, in particular, to advance and represent the interests as well as the development and professionalism of all those practicing in the ICT industry.
                    </p>
                    <ul>
                    <li>Objective <br> The primary objective this framework is to improve ICT professional by ensuring ICT professionals practice by adhering to highest professional standards.  Specifically, the policy framework intends to:<br>
                    ->Enhance the quality of ICT human resources and professional services;<br>
                    ->Raise the profile of the ICT profession;<br>
                    ->Provide a common methodology in assessing/reviewing ICT capabilities;<br>
                    ->Attract more young people to join ICT profession;<br>
                    ->Facilitate professional career development;<br>
                    ->Advance Tanzania’s position as a leading knowledge based society.
                    </li>
                    <span id="dots">...</span><span id="more">
                    <li>Scope Of Registration <br>The registration process applies to ICT Professionals and Practitioners undertaking ICT activities in the country.<br>In the context of the framework guiding the registration process, an ICT Professional is  “a person with proven relevant skills who is registered by the Commission to perform ICT professional activities related to Information technology, computer and communication systems that includes hardware, software, ICT management, data communication and management, signal processing, security, electronics, analytics, Internet, ICT Infrastructure, e-services and other related ICT areas".</li>
                    <li>Qualifications <br>Applicants who undertook academic trainings in Tanzania and abroad shall be evaluated based on awards or certification recognized by TCU, NACTE and VETA.</li>
                    <li>Registration By Tanzanians <br></li>
                    <li>Certified ICT Consultant <br>The category caters for individuals who have passed ICTC certification examination, attained high standards and have strong ICT analytical skills to comprehend complex issues, analyze and develop solutions, provide professional technical support, able to conduct trainings and consultation services in ICT. An individual in this category shall be required to fulfill the following requirements:<br>Must pass Commission’s certifications or be registered as an ICT Professional for at least three years or have otherwise satisfied the Commission to have attained demonstrable professional successes;<br>Submit two letters of referees who are registered as ICT Consultants or support letter from the Ministry responsible for ICT, at initial stage, to prove competence of an applicant in a specified professional area and ability to undertake Level 4 responsibilities described by the guiding registration Framework;<br>An individual shall be recognized as an ICT Consultant for a period of not more than three years during which he/she shall be required to undertake continuous professional practice and fulfill the required professional development hours (PDH) as described in this Framework.</li>
                    <li>ICT Professional <br>An individual shall be recognized as an ICT Professional for a period of not more than three years during which an individual shall be required to undertake continuous professional practice and fulfill the required Professional Development Hours (PDH) as defined in the guiding registration framework.<br>An individual to be registered as ICT Professional shall be required to have either of the following:<br>A degree in ICT related program offered by an institution recognized by TCU or NACTE; proven minimum of three consecutive years of practice and experience in the ICT field;or<br>A diploma in ICT related program offered by an institution recognized by NACTE, proven minimum of five consecutive years of practice experience after graduation or have passed relevant certification in a professional training center recognized by ICTC;<br>Must have passed ICT professional certification examination equivalent to advanced skills described in the National ICT Skills Development Framework (NISDF) from a training center recognized by the Commission;</li>
                    <li>ICT Graduate <br>An individual need to have a degree in ICT related programs from an institution recognized by TCU or NACTE in order to be recognized as an ICT Graduate. An individual shall be recognized as a ICT Graduate Practitioner for a period of not more than three years during which the person shall be required to undertake continuing professional development and fulfill the required professional development hours (PDH) defined in the guiding registration Framework in order to maintain the registration status.</li>
                    <li>ICT Technician<br>To be recognized as an ICT Technician, an individual must have either of the following:<br>A Diploma in ICT related programs from an institution recognized by NACTE;<br>A Certificate in ICT related programs from an institution recognized by NACTE and has at least ONE year of ICT practice experience in the ICT field.<br>An individual recognized as an ICT Technician shall be required to undertake continuous professional practice and fulfill the required professional development hours (PDH) defined in the guiding registration Framework in order to maintain the registration status.</li>
                    <li>Registration By Non Tanzanians <br>This category is for individuals who are non-Tanzanians and are practicing ICT activities in Tanzania. The duration of registration of Non-Tanzanians as professional shall depend on the work permit issued to the applicant by relevant authorities.  The registration shall be temporary on yearly basis renewable based on need.  In addition to the previous stated requirements for Tanzanians, a non-Tanzanian who wish to practice ICT activities in Tanzania must fulfill the following additional requirements</li>
                    <li>Temporary ICT Consultants<br>The applicant must provide-<br>Provide proof of registration/practice as an ICT Consultant from the country of origin or proof of ability to perform Level 4 responsibilities as stipulated in the guiding registration framework;<br>Two letters of referees (At least one must be a Tanzanian registered as an ICT Consultant to prove competence in a specified professional area of ICT practice;<br>Copy of residence permit or VISA; and Original or certified copies of academic certificates translated in English (should be certified by relevant body from applicant’s home country).</li>
                    <li>Temporary ICT Professionals <br>The applicant must provide: -<br>Proof of registration by an ICT Professional body from the country of origin or proof of ability to perform Level 3 responsibilities as stipulated in the guiding registration framework;<br>Three letters of referees (two must be Tanzanians registered as ICT Professionals/ICT Consultants) endorsing competency of applicant in a specified professional area of ICT practice);<br>Copy of residence permit or VISA; and Original or certified copies of academic certificates translated in English (should be certified by relevant body from applicant’s home country).</li>
